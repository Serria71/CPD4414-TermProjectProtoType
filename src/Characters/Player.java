package Characters;

/**
 *
 * @author c0640785
 */
public class Player {
    public static double Strength;
    public static double Dexterity;
    public static double Constitution;
    public static double Intelligence;
    public static double Wisdom;
    public static double Charisma;
    
    public Player(double Str, double Dex, double Con, double Int, double Wis, double Cha) {
        Strength = Str;
        Dexterity = Dex;
        Constitution = Con;
        Intelligence = Int;
        Wisdom = Wis;
        Charisma = Cha;
    }
    
}
